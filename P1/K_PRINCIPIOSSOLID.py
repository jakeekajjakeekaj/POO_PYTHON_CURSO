# Los principios SOLID (SOLID PRINCIPLES)

# Los principios solid:
# - Hasta dónde va a llegar el código? (su escalabilidad)
#- Mantenibilidad (se deben seguir buenas prácticas)
#- Reusabilidad (Nuestros módlos deben de poder usarse múltiples veces)
#- Legibilidad ( El código debe ser legible y comentado)
#- Extensible (Se puede extener sin afectar la eficacia y efectividad del programa)

# De ahí vienen los principios SOLID, los cuáles son pensados para POO, y el nombre viene por sus iniciales, estas serían:
#S: SRP, PRINCIPIO DE RESPONSABILIDAD ÚNICA
#O: OCP, PRINCIPIO DE ABIERTO/CERRADO
#L: LSP, PRINCIPIO DE SUSTITUCIÓN DE LISKOV
#I: ISP, PRINCIPIO DE SEGREGACIÓN DE INTERFAZ
#D: DIP, PRINCIPIO DE INVERSIÓN DE DEPENDENCIAS

#Estos principios fueron pensados principalmente para POO de lenguajes complidados como JAVA O C++, pero estos también se aplican a estos lenguajes interpretaos como lo que sería Python
#El simple hecho de utilizar SOLID ya es considerado lo que sería una buena práctica.